    private void jButtonGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGrabarActionPerformed
        // Verificamos que tenemos nombre del cliente y descripción del lugar de visita
        if(jTextFieldNomCliente.getText().isEmpty() == false && jTextFieldDescLugar.getText().isEmpty() == false) {
            Lugares lugar = new Lugares();
            // Verificamos si se ha seleccionado un lugar de visita del maestro
            if(jLabelIdLugar.getText().equals("")) {
                if(idCliente == 0) {
                    idLugar = lugar.grabarLugar("INSERT INTO lugares VALUES(?,'" + jTextFieldDescLugar.getText() + 
                                                    "','" + jTextFieldDirLugar.getText() + "','" + jTextFieldCPLugar.getText() + "','" + jTextFieldPobLugar.getText() + 
                                                    "','" + jTextFieldProLugar.getText() + "','" + jTextFieldTelLugar.getText() + 
                                                    "','" + jTextFieldFaxLugar.getText() + "','" + jTextFieldEmailLugar.getText() + 
                                                    "','" + jTextFieldBrickLugar.getText() + "','','','')");
                } else {
                    lugar.actualizarLugar("UPDATE lugares SET descripcion='" + jTextFieldDescLugar.getText() + "',direccion='" + jTextFieldDirLugar.getText() +
                                            "',cod_postal='" + jTextFieldCPLugar.getText() + "',poblacion='" + jTextFieldPobLugar.getText() + 
                                            "',provincia='" + jTextFieldProLugar.getText() + "',telefono='" + jTextFieldTelLugar.getText() + 
                                            "',fax='" + jTextFieldFaxLugar.getText() + "',email='" + jTextFieldEmailLugar.getText() + 
                                            "',brick='" + jTextFieldBrickLugar.getText() + "' WHERE id=" + idLugar);
                }
            } else {
                idLugar = Integer.parseInt(jLabelIdLugar.getText());
                lugar.actualizarLugar("UPDATE lugares SET descripcion='" + jTextFieldDescLugar.getText() + "',direccion='" + jTextFieldDirLugar.getText() +
                                        "',cod_postal='" + jTextFieldCPLugar.getText() + "',poblacion='" + jTextFieldPobLugar.getText() + 
                                        "',provincia='" + jTextFieldProLugar.getText() + "',telefono='" + jTextFieldTelLugar.getText() + 
                                        "',fax='" + jTextFieldFaxLugar.getText() + "',email='" + jTextFieldEmailLugar.getText() + 
                                        "',brick='" + jTextFieldBrickLugar.getText() + "' WHERE id=" + idLugar);
            }
            if(idLugar != 0) {
                Clientes cliente = new Clientes();
                if(idCliente == 0) {
                    idCliente = cliente.grabarCliente("INSERT INTO clientes VALUES(?,'" + jTextFieldNomCliente.getText() + 
                                                        "','" + jTextFieldDirCliente.getText() + "','" + jTextFieldCPCliente.getText() + 
                                                        "','" + jTextFieldPobCliente.getText() + "','" + jTextFieldProCliente.getText() + 
                                                        "','" + jTextFieldTelCliente.getText() + "','" + jTextFieldFaxCliente.getText() + 
                                                        "','" + jTextFieldEmailCliente.getText() + "','" + jTextAreaOtrosDatos.getText() + "'," + idLugar + ")");
                    
                    SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy'-'MM'-'dd", new Locale("es_ES"));
                    Date fecha = new Date();
                    String fechaActualFormateada = formatoFecha.format(fecha);
                    System.out.println(fechaActualFormateada);
                    
                    Visitas visita = new Visitas();
                    if(visita.grabarVisita("INSERT INTO visitas VALUES(?,date('" + fechaActualFormateada + "'),'Alta inicial',1," + idCliente + ")")) { 
                        JOptionPane.showMessageDialog(null, "Cliente grabado con éxito", "Información", JOptionPane.INFORMATION_MESSAGE, new ImageIcon("src/iconos/information.png"));
                    }
                } else {
                    cliente.actualizarCliente("UPDATE clientes SET nombre='" + jTextFieldNomCliente.getText() + 
                                                "',direccion='" + jTextFieldDirCliente.getText() + "',cod_postal='" + jTextFieldCPCliente.getText() + 
                                                "',poblacion='" + jTextFieldPobCliente.getText() + "',provincia='" + jTextFieldProCliente.getText() + 
                                                "',telefono='" + jTextFieldTelCliente.getText() + "',fax='" + jTextFieldFaxCliente.getText() + 
                                                "',email='" + jTextFieldEmailCliente.getText() + "',otros_datos='" + jTextAreaOtrosDatos.getText() + "',id_lugar=" + idLugar + " WHERE id=" + idCliente);
                    JOptionPane.showMessageDialog(null, "Cliente actualizado con éxito", "Información", JOptionPane.INFORMATION_MESSAGE, new ImageIcon("src/iconos/information.png"));
                }
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Indique un lugar de visita", "Aviso", JOptionPane.ERROR_MESSAGE, new ImageIcon("src/iconos/warning.png")); 
            }
        } else {
            JOptionPane.showMessageDialog(null, "Rellene al menos los campos 'Nombre' y 'Descripción'", "Aviso", JOptionPane.ERROR_MESSAGE, new ImageIcon("src/iconos/warning.png")); 
        }
    }//GEN-LAST:event_jButtonGrabarActionPerformed

