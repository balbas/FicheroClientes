                jTextFieldFaxCliente.setText(rs.getString(8));
                jTextFieldEmailCliente.setText(rs.getString(9));
                jTextAreaOtrosDatos.setText(rs.getString(10));
                
                jLabelIdLugar.setText(rs.getString(12));
                idLugar = Integer.parseInt(jLabelIdLugar.getText());
                jTextFieldDescLugar.setText(rs.getString(13));
                jTextFieldDirLugar.setText(rs.getString(14));
                jTextFieldCPLugar.setText(rs.getString(15));
                jTextFieldPobLugar.setText(rs.getString(16));
                jTextFieldProLugar.setText(rs.getString(17));
                jTextFieldTelLugar.setText(rs.getString(18));
                jTextFieldFaxLugar.setText(rs.getString(19));
